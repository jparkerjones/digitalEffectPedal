/**
  * @brief This function handles the 2 interrupt functions' actions for RotEnc 2.
  */
void Rotary_Encoder_Handler2(void)
{
	int currenta = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_6);
	int currentb = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_7);

	if (currenta != lasta2) {
		if (currentb != currenta) {
			position2++;
		}
		else {
			position2--;
		}
	}
	lasta2 = currenta;
}

